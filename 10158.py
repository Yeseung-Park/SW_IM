# GPT가 추천해준 굉장히 쉬운 방법

w, h = map(int, input().split())  # w: 가로 길이, h: 세로 길이
p, q = map(int, input().split())  # p: 초기 x, q: 초기 y
t = int(input())  # t: 시간

# 개미의 이동 후 x, y 좌표는 w와 h의 주기에 따라 반복되므로
# (p+t) // w로 몫을 구해서 홀수번이면 반대로 가도록 하고,
# 나머지를 이용해 실제 좌표를 구함

x = (p + t) % (2 * w)
# x = p + (t % (2*w))
y = (q + t) % (2 * h)
# y = q + (t % (2*h))

# 왜 주석 처리한걸로 돌리면 틀리는거지 바로?
# 주기와 반사를 동시에 고려할 수 없기 때문에...
# 근데 이해가 안 가...

# x 또는 y가 w나 h보다 크면 반사된 부분이므로 전체 크기에서 빼줌
# 여기가 이해가 안 가...
# 그냥 이해 안 가는대로 살까...
if x > w:
    x = 2 * w - x
if y > h:
    y = 2 * h - y

print(x, y)